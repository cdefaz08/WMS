        left_col = QtWidgets.QVBoxLayout()
        left_col.addWidget(self.general_group)
        left_col.addWidget(self.capacity_group)
        left_col.addWidget(self.limits_group)

        right_col = QtWidgets.QVBoxLayout()
        right_col.addWidget(self.coords_group)
        right_col.addWidget(self.prox_group)
        right_col.addWidget(self.flags_group)

        main_split = QtWidgets.QHBoxLayout()
        main_split.addLayout(left_col, 3)
        main_split.addLayout(right_col, 2)

        self.main_layout.addLayout(main_split)

        # Footer ocupa todo el ancho abajo
        self.main_layout.addSpacing(10)
        self.main_layout.addLayout(footer_layout)