from PyQt5 import QtWidgets

class Ui_AddLocation(QtWidgets.QDialog):
    def __init__(self, parent=None):
        super().__init__(parent)
        self.setWindowTitle("Add New Location")
        self.resize(900, 600)
        self.main_layout = QtWidgets.QVBoxLayout(self)

        # === General Info ===
        self.general_group = QtWidgets.QGroupBox("General Info")
        form_general = QtWidgets.QFormLayout()
        self.lineEdit_Location = QtWidgets.QLineEdit()
        self.lineEdit_ScanLocation = QtWidgets.QLineEdit()
        self.comboBox_LocationType = QtWidgets.QComboBox()
        self.comboBox_PutawayClass = QtWidgets.QComboBox()
        self.comboBox_PickClass = QtWidgets.QComboBox()
