        form_general.addRow("Scan Location:", self.lineEdit_ScanLocation)
        form_general.addRow("Location Type:", self.comboBox_LocationType)
        form_general.addRow("Putaway Class:", self.comboBox_PutawayClass)
        form_general.addRow("Pick Class:", self.comboBox_PickClass)
        form_general.addRow("Restock Class:", self.comboBox_RestockClass)
        self.general_group.setLayout(form_general)


        # === Coordinates (Vertical layout) ===
        self.coords_group = QtWidgets.QGroupBox("Physical Coordinates")
        form_coords = QtWidgets.QFormLayout()
        self.lineEdit_Aisle = QtWidgets.QLineEdit()
        self.lineEdit_Bay = QtWidgets.QLineEdit()
        self.lineEdit_Level = QtWidgets.QLineEdit()
        self.lineEdit_Slot = QtWidgets.QLineEdit()
        form_coords.addRow("Aisle:", self.lineEdit_Aisle)
        form_coords.addRow("Bay:", self.lineEdit_Bay)
        form_coords.addRow("Level:", self.lineEdit_Level)
        form_coords.addRow("Slot:", self.lineEdit_Slot)
        self.coords_group.setLayout(form_coords)

        # === Capacity ===
        self.capacity_group = QtWidgets.QGroupBox("Capacity")
        form_capacity = QtWidgets.QFormLayout()
        self.lineEdit_PalletCap = QtWidgets.QLineEdit()
        self.lineEdit_CartonCap = QtWidgets.QLineEdit()
        form_capacity.addRow("Pallet Capacity:", self.lineEdit_PalletCap)
        form_capacity.addRow("Carton Capacity:", self.lineEdit_CartonCap)
        self.capacity_group.setLayout(form_capacity)

        # === Max Dimensions / Weight ===
        self.limits_group = QtWidgets.QGroupBox("Max Dimensions / Weight")
        grid_limits = QtWidgets.QGridLayout()
        self.lineEdit_MaxHeight = QtWidgets.QLineEdit()
        self.comboBox_HeightUOM = QtWidgets.QComboBox()
        self.lineEdit_MaxDepth = QtWidgets.QLineEdit()
        self.comboBox_DepthUOM = QtWidgets.QComboBox()
        self.lineEdit_MaxWidth = QtWidgets.QLineEdit()
        self.comboBox_WidthUOM = QtWidgets.QComboBox()
        self.lineEdit_MaxWeight = QtWidgets.QLineEdit()
        self.comboBox_WeightUOM = QtWidgets.QComboBox()
        grid_limits.addWidget(QtWidgets.QLabel("Max Height:"), 0, 0)
        grid_limits.addWidget(self.lineEdit_MaxHeight, 0, 1)
        grid_limits.addWidget(self.comboBox_HeightUOM, 0, 2)
        grid_limits.addWidget(QtWidgets.QLabel("Max Depth:"), 1, 0)
        grid_limits.addWidget(self.lineEdit_MaxDepth, 1, 1)
        grid_limits.addWidget(self.comboBox_DepthUOM, 1, 2)
        grid_limits.addWidget(QtWidgets.QLabel("Max Width:"), 2, 0)
        grid_limits.addWidget(self.lineEdit_MaxWidth, 2, 1)
        grid_limits.addWidget(self.comboBox_WidthUOM, 2, 2)
        grid_limits.addWidget(QtWidgets.QLabel("Max Weight:"), 3, 0)
        grid_limits.addWidget(self.lineEdit_MaxWeight, 3, 1)
        grid_limits.addWidget(self.comboBox_WeightUOM, 3, 2)
        self.limits_group.setLayout(grid_limits)

        # === Proximity ===
        self.prox_group = QtWidgets.QGroupBox("Proximity Settings")
        form_prox = QtWidgets.QFormLayout()
        self.comboBox_BlockCode = QtWidgets.QComboBox()
        self.comboBox_ProxIN = QtWidgets.QComboBox()
        self.comboBox_ProxOUT = QtWidgets.QComboBox()
        self.lineEdit_PnD1 = QtWidgets.QLineEdit()
        self.lineEdit_PnD2 = QtWidgets.QLineEdit()
        form_prox.addRow("Block Code:", self.comboBox_BlockCode)
        form_prox.addRow("Proximity In:", self.comboBox_ProxIN)
        form_prox.addRow("Proximity Out:", self.comboBox_ProxOUT)
        form_prox.addRow("PnD Location 1:", self.lineEdit_PnD1)
        form_prox.addRow("PnD Location 2:", self.lineEdit_PnD2)
        self.prox_group.setLayout(form_prox)

