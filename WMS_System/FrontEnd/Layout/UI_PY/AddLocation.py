        footer_layout = QtWidgets.QHBoxLayout()
        self.lineEdit_LastTouched = QtWidgets.QLineEdit()
        self.lineEdit_LastTouched.setReadOnly(True)
        self.lineEdit_PalletsQty = QtWidgets.QLineEdit()
        self.lineEdit_PalletsQty.setReadOnly(True)
        self.lineEdit_CartonsQty = QtWidgets.QLineEdit()
        self.lineEdit_CartonsQty.setReadOnly(True)
        footer_layout.addWidget(QtWidgets.QLabel("Last touched:"))
        footer_layout.addWidget(self.lineEdit_LastTouched)
        footer_layout.addSpacing(30)
        footer_layout.addWidget(QtWidgets.QLabel("Active Pallets QTY:"))
        footer_layout.addWidget(self.lineEdit_PalletsQty)
        footer_layout.addWidget(QtWidgets.QLabel("Active Cartons QTY:"))
        footer_layout.addWidget(self.lineEdit_CartonsQty)

