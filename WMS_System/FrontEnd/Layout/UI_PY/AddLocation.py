        self.flags_group = QtWidgets.QGroupBox("Status Flags")
        layout_flags = QtWidgets.QVBoxLayout()
        self.checkBox_BlackHole = QtWidgets.QCheckBox("Black Hole")
        self.checkBox_Assigned = QtWidgets.QCheckBox("Has assign flag")
